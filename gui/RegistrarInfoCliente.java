    private void cEstadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cEstadoActionPerformed
        String elementoSel = (String) cEstado.getSelectedItem();
        if (!elementoSel.equals("")) {
            String abr = estadoAbr.get(elementoSel);
            TreeSet<String> cidades = estadosCidades.get(abr);
            DefaultListModel<String> modelo = new DefaultListModel<>();
            for (String cidade : cidades) {
                modelo.addElement(cidade);
            }
            lCidade.setModel(modelo);
        }
    }//GEN-LAST:event_cEstadoActionPerformed

    private void tCepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tCepActionPerformed
        String cepDigitado = tCep.getText();
        cepDigitado = cepDigitado.replaceAll("-", "");
        if (!cepCidadeEstado.containsKey(cepDigitado)) {
            JOptionPane.showMessageDialog(null, "CEP InvÃ¡lido");
            tCep.requestFocus();
        } else {
            CidadeEstado cidEst = cepCidadeEstado.get(cepDigitado);
            cEstado.setSelectedItem(cidEst.getEstado());
            lCidade.setSelectedValue(cidEst.getCidade(), true);
        }
    }//GEN-LAST:event_tCepActionPerformed

