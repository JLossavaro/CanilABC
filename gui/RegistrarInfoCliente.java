    public void carregarInfos() {
        try {
            cEstado.addItem("");
            BufferedReader arq = new BufferedReader(new FileReader("ceps.txt"));
            String linha = "";
            while ((linha = arq.readLine()) != null) {
                String[] partes = linha.split("\t");
                String cep = partes[0];
                String cidEst = partes[1];
                String[] partes2 = cidEst.split("/");
                String cidade = partes2[0];
                String estadoTemp = partes2[1];
                String estado = estadoTemp.split(" - ")[0].trim();
                cepCidadeEstado.put(cep, new CidadeEstado(cidade, abrEstado.get(estado)));
                if (estadosCidades.containsKey(estado)) {
                    estadosCidades.get(estado).add(cidade);
                } else {
                    TreeSet<String> cidades = new TreeSet<>();
                    cidades.add(cidade);
                    estadosCidades.put(estado, cidades);
                }
            }
            arq.close();
        } catch (IOException ex) {
            System.err.println("Deu pau!");
            ex.printStackTrace();
        }
    }

    public void carregarEstados() {
        try {
            cEstado.addItem("");
            BufferedReader arq = new BufferedReader(new FileReader("estados.txt"));
            String linha = "";
            while ((linha = arq.readLine()) != null) {
                String[] partes = linha.split("\t");
                String nomeEstado = partes[0];
                String abr = partes[1];
                estadoAbr.put(nomeEstado, abr);
                abrEstado.put(abr, nomeEstado);
                cEstado.addItem(nomeEstado);
            }
            arq.close();
        } catch (IOException ex) {
            System.err.println("Deu pau!");
            ex.printStackTrace();
        }
    }

